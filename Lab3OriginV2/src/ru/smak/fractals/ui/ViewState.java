package ru.smak.fractals.ui;

class ViewState {
    final double xMin;
    final double xMax;
    final double yMin;
    final double yMax;

    public ViewState(double xMin, double xMax, double yMin, double yMax) {
        this.xMin = xMin;
        this.xMax = xMax;
        this.yMin = yMin;
        this.yMax = yMax;
    }
}
